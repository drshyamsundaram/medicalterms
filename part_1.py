def sample_merged_dataset(input_json_path: str, n_sample: int, output_sampled_json_path: str):
    """
    Samples N_sample rows from a merged JSON dataset and saves it to a new file.

    Args:
        input_json_path: Path to the full merged JSON dataset.
        n_sample: Number of samples to draw.
        output_sampled_json_path: Path to save the sampled JSON dataset.
    """
    print(f"\n--- Sampling {n_sample} examples from '{input_json_path}' ---")
    all_examples = []
    try:
        with jsonlines.open(input_json_path, mode='r') as reader:
            for obj in reader:
                all_examples.append(obj)
    except FileNotFoundError:
        print(f"Error: Input file '{input_json_path}' not found.")
        return

    if not all_examples:
        print("No examples found in the input file. Skipping sampling.")
        return

    if n_sample >= len(all_examples):
        print(f"Warning: Sample size ({n_sample}) is greater than or equal to total examples ({len(all_examples)}). Saving all examples as sampled.")
        sampled_data = all_examples
    else:
        sampled_data = random.sample(all_examples, n_sample)

    with jsonlines.open(output_sampled_json_path, mode='w') as writer:
        writer.write_all(sampled_data)
    print(f"Sampled data ({len(sampled_data)} examples) saved to '{output_sampled_json_path}'")

"""### 5. Orchestration Function to Perform All Steps

Finally, we'll encapsulate all the above logic into a single, modular function that can be called to perform the entire process: loading, converting to common format, merging, and saving both JSON and JSON-LD outputs.

"""

def process_medical_datasets(dataset_config: dict, build_config: dict):
    """Orchestrates the loading, merging, and saving of medical datasets.

    Args:
        dataset_config: The configuration dictionary for datasets (DATASET_CONFIG).
        build_config: The configuration dictionary for file names and sampling.
    """
    print("--- Starting Medical Dataset Processing ---")

    # Extract filenames from build_config
    output_json_filename = build_config.get('output_json_filename', 'merged_medical_datasets.json')
    output_jsonld_filename = build_config.get('output_jsonld_filename', 'merged_medical_datasets.jsonld')
    sample_size = build_config.get('sample_size', 100)
    sampled_output_json_filename = build_config.get('sampled_output_json_filename', 'sampled_medical_datasets.json')

    # 1. Load all datasets
    all_loaded_datasets = {}
    for key in dataset_config:
        dataset_splits = load_medical_dataset(key)
        if dataset_splits:
            all_loaded_datasets[key] = dataset_splits
    print(f"\nSuccessfully loaded {len(all_loaded_datasets)} out of {len(dataset_config)} datasets after initial loading attempts.")

    # 2. Merge datasets to common format
    merged_medical_data = merge_datasets_to_common_format(all_loaded_datasets)
    print(f"\nTotal merged examples: {len(merged_medical_data)}")

    # 3. Save full merged data to JSON
    with jsonlines.open(output_json_filename, mode='w') as writer:
        writer.write_all(merged_medical_data)
    print(f"Full merged data saved to '{output_json_filename}'")

    # 4. Convert and save to JSON-LD
    merged_medical_data_jsonld = convert_to_jsonld(merged_medical_data)
    with jsonlines.open(output_jsonld_filename, mode='w') as writer:
        writer.write_all(merged_medical_data_jsonld)
    print(f"JSON-LD data saved to '{output_jsonld_filename}'")

    # 5. Sample the merged dataset
    sample_merged_dataset(output_json_filename, sample_size, sampled_output_json_filename)

    print("--- Medical Dataset Processing Complete ---")

# Example of how to call the orchestration function
