"""### 4. Create JSON-LD Version of the Merged Data

JSON-LD (JSON for Linking Data) is a lightweight Linked Data format. It's designed to be easily usable by JSON-based data programming environments, and provides a way to express semantically rich metadata. We will convert our common JSON format into a simple JSON-LD structure.

"""

def convert_to_jsonld(common_data_list: list, context_url: str = "https://schema.org/") -> list:
    """Converts a list of common formatted examples into JSON-LD format.

    Args:
        common_data_list: A list of dictionaries in the COMMON_FORMAT_SCHEMA.
        context_url: The URL for the JSON-LD context.

    Returns:
        A list of dictionaries, each formatted as JSON-LD.
    """
    jsonld_data = []
    for example in common_data_list:
        jsonld_entry = {
            "@context": context_url,
            "@id": f"urn:example:{example['id']}",
            "@type": "DatasetRecord", # A generic type for our merged records
            "name": example['text'],
            "description": f"Record of type {example['type']} from {example['metadata'].get('original_dataset', 'unknown dataset')}",
            "keywords": [label['entity_type'] for label in example['labels'] if isinstance(label, dict) and 'entity_type' in label] if example['labels'] else [],
            "url": None, # Placeholder, could be linked to original source if available
            "additionalProperty": []
        }

        # Add 'text' if it exists and is a string
        if 'text' in example and isinstance(example['text'], str):
            jsonld_entry['mainEntity'] = {
                "@type": "CreativeWork",
                "text": example['text']
            }

        # Add labels as additional properties or entities
        if example['labels']:
            for label_entry in example['labels']:
                if isinstance(label_entry, dict):
                    # For structured labels (e.g., NER spans)
                    entity_type = label_entry.get('entity_type', 'Entity')
                    jsonld_entry['additionalProperty'].append({
                        "@type": "PropertyValue",
                        "name": entity_type,
                        "value": label_entry.get('text'),
                        "description": f"Original offset: {label_entry.get('offsets')}" if 'offsets' in label_entry else None
                    })
                else:
                    # For simple string labels
                    jsonld_entry['additionalProperty'].append({
                        "@type": "PropertyValue",
                        "name": "label",
                        "value": str(label_entry)
                    })

        # Add metadata properties
        if example['metadata']:
            for key, value in example['metadata'].items():
                if isinstance(value, (str, int, float, bool)):
                    jsonld_entry['additionalProperty'].append({
                        "@type": "PropertyValue",
                        "name": key,
                        "value": str(value)
                    })
                elif isinstance(value, list):
                    for item in value:
                        jsonld_entry['additionalProperty'].append({
                            "@type": "PropertyValue",
                            "name": key,
                            "value": str(item)
                        })

        jsonld_data.append(jsonld_entry)
    return jsonld_data

