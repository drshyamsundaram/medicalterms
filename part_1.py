"""## 4. Define Common Format and Merge Datasets

To ensure consistency across different medical datasets, we need to define a common, standardized JSON format. This format will facilitate easier analysis, model training, and integration. We'll then create functions to transform each dataset's examples into this common structure.

Our common format will aim to capture essential information like:
- `id`: A unique identifier for the example.
- `text`: The primary text content (e.g., clinical note, question, sentence).
- `labels`: A list of entities or classifications relevant to the text (e.g., disease names, answer types).
- `type`: The type of data (e.g., 'question_answering', 'named_entity_recognition', 'text_classification').
- `metadata`: A dictionary for any additional, dataset-specific information.

"""

# Define the common schema
COMMON_FORMAT_SCHEMA = {
    "id": "",
    "text": "",
    "labels": [],
    "type": "", # e.g., 'named_entity_recognition', 'question_answering', 'text_classification'
    "metadata": {} # For any additional dataset-specific information
}

def _get_spans_from_tokens_and_ner_tags(tokens, ner_tags, id_to_ner_label):
    """Helper to reconstruct text and get entity spans from tokens and NER tags.
    Adapted for datasets that provide tokens and BIO-labeled ner_tags.
    """
    text = " ".join(tokens)
    labels = []
    current_entity = []
    current_type = None
    start_idx = 0

    for i, (token, tag_id) in enumerate(zip(tokens, ner_tags)):
        absolute_start = text.find(token, start_idx)
        if absolute_start == -1: # Should not happen if tokens form the text
             absolute_start = start_idx # Fallback
        absolute_end = absolute_start + len(token)

        tag_name = id_to_ner_label.get(tag_id, 'O')

        if tag_name.startswith('B-'):
            if current_entity:
                labels.append({
                    'text': " ".join(current_entity),
                    'entity_type': current_type,
                    'offsets': {'start': current_start_offset, 'end': current_end_offset}
                })
            current_type = tag_name[2:]
            current_entity = [token]
            current_start_offset = absolute_start
            current_end_offset = absolute_end
        elif tag_name.startswith('I-') and current_type == tag_name[2:]:
            current_entity.append(token)
            current_end_offset = absolute_end
        else:
            if current_entity:
                labels.append({
                    'text': " ".join(current_entity),
                    'entity_type': current_type,
                    'offsets': {'start': current_start_offset, 'end': current_end_offset}
                })
            current_entity = []
            current_type = None

        start_idx = absolute_end # Update start index for next token search
        if i < len(tokens) - 1: # account for space between tokens
            start_idx += 1

    if current_entity:
        labels.append({
            'text': " ".join(current_entity),
            'entity_type': current_type,
            'offsets': {'start': current_start_offset, 'end': current_end_offset}
        })
    return text, labels

def convert_to_common_format(dataset_key: str, example: dict) -> dict:
    """Converts a single example from a specific dataset into the common format.

    Args:
        dataset_key: The key of the dataset in DATASET_CONFIG.
        example: The original example dictionary from the dataset.

    Returns:
        A dictionary formatted according to COMMON_FORMAT_SCHEMA.
    """
    common_example = COMMON_FORMAT_SCHEMA.copy()
    common_example['metadata'] = {'original_dataset': dataset_key}

    if dataset_key == 'ncbi_disease':
        common_example['id'] = str(example['id'])
        ner_tag_labels = {0: 'O', 1: 'B-Disease', 2: 'I-Disease'}
        text, labels = _get_spans_from_tokens_and_ner_tags(example['tokens'], example['ner_tags'], ner_tag_labels)
        common_example['text'] = text
        common_example['type'] = 'named_entity_recognition'
        common_example['labels'] = labels

    elif dataset_key == 'pubmed_qa':
        common_example['id'] = example['pubid'] if 'pubid' in example else ''
        common_example['text'] = example['question']
        common_example['type'] = 'question_answering'
        common_example['labels'] = [example['long_answer']] if 'long_answer' in example else []
        common_example['metadata']['context'] = example['context'] if 'context' in example else ''
        common_example['metadata']['final_decision'] = example['final_decision'] if 'final_decision' in example else ''

    elif dataset_key == 'medqa_usmle': # Updated medical_qa to medqa_usmle
        common_example['id'] = str(example['id'])
        common_example['text'] = example['question']
        common_example['type'] = 'question_answering'
        common_example['labels'] = [example['answer']] if 'answer' in example else []

    elif dataset_key == 'bc5cdr':
        common_example['id'] = str(example['id'])
        ner_tag_labels = {0: 'O', 1: 'B-Disease', 2: 'I-Disease', 3: 'B-Chemical', 4: 'I-Chemical'}
        text, labels = _get_spans_from_tokens_and_ner_tags(example['tokens'], example['ner_tags'], ner_tag_labels)
        common_example['text'] = text
        common_example['type'] = 'named_entity_recognition'
        common_example['labels'] = labels

    elif dataset_key == 'medmcqa':
        common_example['id'] = str(example['id'])
        common_example['text'] = example['question']
        common_example['type'] = 'multiple_choice_qa'
        # For multiple choice, store options and correct answer in metadata
        options = [example['opa'], example['opb'], example['opc'], example['opd']]
        correct_answer_idx = example['cop'] # 0, 1, 2, 3
        correct_answer = options[correct_answer_idx]
        common_example['labels'] = [correct_answer]
        common_example['metadata']['options'] = options
        common_example['metadata']['correct_answer_index'] = correct_answer_idx

    elif dataset_key == 'clinical_notes':
        common_example['id'] = str(example['id'])
        common_example['text'] = example['text']
        common_example['type'] = 'clinical_note'
        common_example['labels'] = [] # This dataset might need further processing for labels
        common_example['metadata']['report_type'] = example['report_type']
        common_example['metadata']['gender'] = example['gender']

    elif dataset_key == 'multi_nli_entailment':
        common_example['id'] = str(example['pairID'])
        common_example['text'] = example['premise'] + " [SEP] " + example['hypothesis']
        common_example['type'] = 'natural_language_inference'
        label_map = {0: 'entailment', 1: 'neutral', 2: 'contradiction'}
        common_example['labels'] = [label_map.get(example['label'], 'unknown')]
        common_example['metadata']['premise'] = example['premise']
        common_example['metadata']['hypothesis'] = example['hypothesis']

    elif dataset_key == 'medical_dialogue_de': # New medical dialogue translation dataset
        common_example['id'] = str(hash(example['translation']['en'] + example['translation']['de'])) # Unique ID
        common_example['text'] = example['translation']['en']
        common_example['type'] = 'machine_translation'
        common_example['labels'] = [example['translation']['de']]
        common_example['metadata']['source_lang'] = 'en'
        common_example['metadata']['target_lang'] = 'de'

    elif dataset_key == 'sem_eval_2010_task_8': # New relation extraction dataset
        # sem_eval_2010_task_8 has integer labels, need to map them to string names
        # The dataset object will have features['labels'].names
        # Assuming we can get the label names dynamically or hardcode if stable.
        # For now, let's hardcode a few common ones if needed, or assume a direct map to string
        # based on the dataset's features. The original dataset provides `labels` as integer indices.
        # The 'labels' feature in `sem_eval_2010_task_8` is a ClassLabel, so example['labels'] will be the integer ID.
        # To get the string name, we would typically need access to the dataset.features['labels'].names
        # For simplicity, assuming a generic conversion for now, or will refine if needed.
        label_names = [
            'Other', 'Cause-Effect(e1,e2)', 'Cause-Effect(e2,e1)',
            'Instrument-Agency(e1,e2)', 'Instrument-Agency(e2,e1)',
            'Product-Producer(e1,e2)', 'Product-Producer(e2,e1)',
            'Content-Container(e1,e2)', 'Content-Container(e2,e1)',
            'Entity-Origin(e1,e2)', 'Entity-Origin(e2,e1)',
            'Entity-Destination(e1,e2)', 'Entity-Destination(e2,e1)',
            'Component-Whole(e1,e2)', 'Component-Whole(e2,e1)',
            'Member-Collection(e1,e2)', 'Member-Collection(e2,e1)'
        ]
        common_example['id'] = str(hash(example['sentence']))
        common_example['text'] = example['sentence']
        common_example['type'] = 'relation_extraction'
        common_example['labels'] = [label_names[example['labels']]] if 'labels' in example and example['labels'] < len(label_names) else []

    elif dataset_key == 'squad':
        common_example['id'] = example['id']
        common_example['text'] = example['context'] + " [SEP] " + example['question']
        common_example['type'] = 'question_answering'
        # For SQuAD, the answer is usually a span within the context
        answers = example['answers']['text']
        answer_starts = example['answers']['answer_start']
        labels = []
        for ans, start in zip(answers, answer_starts):
            labels.append({'text': ans, 'offsets': {'start': start, 'end': start + len(ans)}})
        common_example['labels'] = labels
        common_example['metadata']['context'] = example['context']
        common_example['metadata']['question'] = example['question']

    return common_example


def merge_datasets_to_common_format(loaded_datasets: dict) -> list:
    """Merges all loaded datasets into a single list of examples in the common format.

    Args:
        loaded_datasets: A dictionary of loaded datasets, as returned by load_medical_dataset.

    Returns:
        A list of dictionaries, each formatted according to COMMON_FORMAT_SCHEMA.
    """
    merged_data = []
    for dataset_key, splits in loaded_datasets.items():
        print(f"Processing dataset: {dataset_key}")
        for split_name, dataset_obj in splits.items():
            print(f"  - Processing split: {split_name} (Size: {len(dataset_obj)} examples)")
            # Apply the conversion function across the dataset
            converted_split = dataset_obj.map(
                lambda x: convert_to_common_format(dataset_key, x),
                remove_columns=dataset_obj.column_names # Remove original columns
            )
            # Add split name to metadata for tracking
            for example in converted_split:
                example['metadata']['original_split'] = split_name
                merged_data.append(example)
    return merged_data

