def load_medical_dataset(dataset_key: str) -> dict:
    """Loads a medical dataset from Hugging Face based on the DATASET_CONFIG.

    Args:
        dataset_key: The key corresponding to the dataset in DATASET_CONFIG.

    Returns:
        A dictionary where keys are split names (e.g., 'train', 'validation')
        and values are `datasets.Dataset` objects. Returns an empty dict if
        the dataset_key is not found or loading fails.
    """
    if dataset_key not in DATASET_CONFIG:
        print(f"Error: Dataset key '{dataset_key}' not found in configuration.")
        return {}

    config_entry = DATASET_CONFIG[dataset_key]
    hf_path = config_entry['hf_path']
    splits = config_entry['splits']
    subset = config_entry.get('subset')

    loaded_splits = {}
    print(f"Loading dataset: {hf_path} (Key: {dataset_key})")
    try:
        if subset:
            full_dataset = load_dataset(hf_path, subset)
        else:
            full_dataset = load_dataset(hf_path)

        for split in splits:
            if split in full_dataset:
                loaded_splits[split] = full_dataset[split]
            else:
                print(f"Warning: Split '{split}' not found for dataset '{hf_path}'.")
        print(f"Successfully loaded splits: {list(loaded_splits.keys())} for {dataset_key}")
        return loaded_splits
    except Exception as e:
        print(f"Failed to load dataset '{hf_path}' (Key: {dataset_key}): {e}")
        return {}

